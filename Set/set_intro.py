

# Create a set object

set_object = {1,1,1,2,3}
# I would like to learn, total number of elements in the set.
# We could use len function.

print(len(set_object)) # 3

# We could add new elements to set using add method.

set_object.add("New Element")
print(set_object)

# set object is mutable.
#remove method will remove given value from set object but if given value
# doesn't exist in set oject, it will generate an error.

set_object.remove("New Element")
print(set_object)












